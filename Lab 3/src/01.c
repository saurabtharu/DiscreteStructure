    printf("|_______|_______|_______|_______|_______|______________|__________|__________________________|\n");


    for (int i = 0; i < 8; i++)
    {
        bool a = p[i] && !q[i] || r[i];
        bool b = !p[i] || r[i];
        bool c;
        if( b == 1)
            c = 1;
        else if( a == 0 )
            c = 1;
        else
            c = 0;

        printf("|    %d  |   %d   |   %d   |   %d   |   %d   |       %d      |     %d    |              %d           |\n", p[i], q[i], r[i], !p[i], !q[i], a, b, c);
    }
        printf("|_______|_______|_______|_______|_______|______________|__________|__________________________|\n");




    printf("\n\nb. (p ᴧ ⌐q ᴠ r) ↔ (⌐p ᴠ r)\n");
